#type casting = convert the datatype fo a value to another data type

x = 1     #int
y = 2.4   #float
z = "3"   #string

x = str(x)    #str() will convert to a string
y = str(y)
z = str(z)


x = int(x)    #int() will convert to an integer
y = int(y)
z = int(z)


x = float(x)    #float() will convert to a float
y = float(y)
z = float(z)
print(x)
print(int(y))
print(z)